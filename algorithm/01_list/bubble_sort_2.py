arr = [90, 55, 7, 78, 12, 42, 3]

for end in range(len(arr) - 1, 0, -1):
    for i in range(end):
        if arr[i] > arr[i + 1]:
            arr[i], arr[i + 1] = arr[i + 1], arr[i]
    # print(arr)

print(arr)

